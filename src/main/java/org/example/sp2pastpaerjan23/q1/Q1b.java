package org.example.sp2pastpaerjan23.q1;

public class Q1b {

    //The four visibilities for an instance variable are as follows.
    //
    //Private:
    private int x;
    //This variable can be accessed only inside the same class.
    //
    //Default or package visibility:

    int x;

//    This variable can be accessed by classes
//    in the same package, but not from other
//        packages.Protected:

    protected int x;
//This variable can be accessed in the same package,
// and also in subclasses even if those subclasses
// are in a different package.

//    Public:

    public int x;
//
//    This variable can be accessed from
//    anywhere that the class itself is accessible.



}
